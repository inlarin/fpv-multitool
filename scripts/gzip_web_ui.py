"""Pre-build: read data/index.html, gzip it, generate src/web/web_ui_gz.h.

The HTML/CSS/JS lived inline in src/web/web_ui.cpp as a 5000-LOC raw-string
literal until v0.28.5 — it has been extracted to data/index.html so the
file can be edited with proper syntax highlighting / linting / dead-code
detection. This script is the build glue: it gzips the source HTML at
compile time and emits a C array header that web_server.cpp serves with
Content-Encoding: gzip.

Output: src/web/web_ui_gz.h
  - const uint8_t WEB_INDEX_HTML_GZ[] PROGMEM = {...};
  - const size_t WEB_INDEX_HTML_GZ_LEN;

Gzip with -9 brings 250 KB of HTML down to ~15 KB on the wire — 5x faster
on WiFi. PROGMEM keeps it in flash, not RAM.
"""
import gzip
import os

Import("env")  # noqa: F821

PROJECT_DIR = env["PROJECT_DIR"]  # noqa: F821
SRC = os.path.join(PROJECT_DIR, "data", "index.html")
OUT = os.path.join(PROJECT_DIR, "src", "web", "web_ui_gz.h")


def read_html(path):
    with open(path, "rb") as f:
        return f.read()


def generate_header(raw):
    gz = gzip.compress(raw, compresslevel=9)
    lines = ["// AUTO-GENERATED by scripts/gzip_web_ui.py from data/index.html — do not edit.",
             "#pragma once",
             "#include <Arduino.h>",
             "",
             f"// Source: {len(raw)} bytes  Gzipped: {len(gz)} bytes  Ratio: {len(gz)*100/len(raw):.1f}%",
             "const uint8_t WEB_INDEX_HTML_GZ[] PROGMEM = {"]
    for i in range(0, len(gz), 16):
        chunk = gz[i:i+16]
        lines.append("    " + ", ".join(f"0x{b:02X}" for b in chunk) + ",")
    lines.append("};")
    lines.append(f"const size_t WEB_INDEX_HTML_GZ_LEN = {len(gz)};")
    lines.append("")
    return "\n".join(lines), len(raw), len(gz)


def main():
    if not os.path.isfile(SRC):
        raise RuntimeError(f"data/index.html not found at {SRC}")
    raw = read_html(SRC)
    header, orig_sz, gz_sz = generate_header(raw)

    # Skip rewrite if output is identical (avoids forcing relink).
    if os.path.exists(OUT):
        with open(OUT, "r", encoding="utf-8") as f:
            if f.read() == header:
                print(f"[gzip_web_ui] unchanged ({orig_sz}B -> {gz_sz}B)")
                return

    with open(OUT, "w", encoding="utf-8") as f:
        f.write(header)
    print(f"[gzip_web_ui] data/index.html -> web_ui_gz.h  ({orig_sz}B -> {gz_sz}B, {gz_sz*100/orig_sz:.1f}%)")


main()
