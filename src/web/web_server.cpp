        uint16_t from = req->hasParam("from") ? (uint16_t)strtoul(req->getParam("from")->value().c_str(), nullptr, 0) : 0x0100;
        uint16_t to   = req->hasParam("to")   ? (uint16_t)strtoul(req->getParam("to")->value().c_str(), nullptr, 0)   : 0x01FF;
